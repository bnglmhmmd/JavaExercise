package bolum01;

public class ArttýrmaAzaltmaOperatör {
	public static void main(String[] args) {
		int i = 3;
		int j = 3;
		
		i++;
		j--;
		
		System.out.println(i);
		System.out.println(j);
		
		
		int a = 10;
		
		int var = a;
		
		System.out.println(a);
		
		a = var++;
		
		System.out.println(a);
		System.out.println(var);
		
		
		
		
		
		
	}

}
